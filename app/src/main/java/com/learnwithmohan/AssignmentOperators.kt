package com.learnwithmohan

fun main() {

    var total = 0

    total += 5       //total = total + 5
    println(total)   //Output 5

    total -= 3       //total = total - 3
    println(total)  //Output 2

    total *= 25     //total = total * 25
    println(total)  //Output 50

    total /= 5      //total = total / 5
    println(total)  //Output 10

    total %= 3      //total = total % 3
    println(total)  //Output 1
}