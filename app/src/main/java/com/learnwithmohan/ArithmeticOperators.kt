package com.learnwithmohan

fun main() {

    val firstNumber = 19
    val secondNumber = 3

    val text1 = "Start"
    val text2 = " Middle "
    val text3 = "End"

    println("+ - * / %")
    println("$firstNumber + $secondNumber = ${firstNumber + secondNumber}")
    println("$firstNumber - $secondNumber = ${firstNumber - secondNumber}")
    println("$firstNumber * $secondNumber = ${firstNumber * secondNumber}")
    println("$firstNumber / $secondNumber = ${firstNumber / secondNumber}")
    println("$firstNumber % $secondNumber = ${firstNumber % secondNumber}")

    println(text1 + text2 + text3)
}